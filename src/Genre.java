// Genre class defines an enum of different movie genres.
public class Genre
{
    /**
     * Enum representing various movie genres.
     * Each constant in the GenreType enum corresponds to a specific genre category
     * used to classify movies within the system.
     */
    public enum GenreType
    {
        ACTION,
        ADVENTURE,
        ANIMATION,
        COMEDY,
        CRIME,
        DOCUMENTARY,
        DRAMA,
        FAMILY,
        FANTASY,
        HISTORY,
        HORROR,
        MUSIC,
        MYSTERY,
        ROMANCE,
        SCIENCE_FICTION,
        TV_MOVIE,
        THRILLER,
        WAR,
        WESTERN
    }
}
